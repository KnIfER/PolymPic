package com.knziha.polymer.Utils;

/** Data struct that consists of two integer number and a string object. */
public class IISTri {
	public int i1;
	public int i2;
	public String str1;
	public String plc;
	
	public IISTri(int i1, int i2, String str1, String plc) {
		this.i1 = i1;
		this.i2 = i2;
		this.str1 = str1;
		this.plc = plc;
	}
}
